// variable ตัวแปร
// เป็นชื่อที่ตั้งเอง ใช้เก็บข้อมูล
// การใช้งานตัวแปร คือ เอาค่าไปเก็บในตัวแปร หรือ เอาค่าในตัวแปรมาใช้
//------------------------------
// var
var aa = 10;
console.log(aa);

// let
let bb = 20;
console.log(bb);

// const    
const cc = 30;
console.log(cc);

aa = 100;
console.log(aa);
bb = 200;
console.log(bb);
