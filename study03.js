// data type ชนิดข้อมูล
// คือ ตัวข้อมูล (literal) ที่เก็บไว้ในตัวแปร (variable) หรือที่ทำไปใช้งาน
//------------------------------
// string
let aa = 'A';
console.log(aa);

// number
let dd = 111;   
console.log(dd);

// boolean
let ee = true;
console.log(ee);
let ff = false;
console.log(ff);

// object
let gg = {
    name: 'AA',
    age: 20
};
console.log(gg);

// array
let hh = [1, 2, 3];
console.log(hh);
console.log(hh[0]);
console.log(hh[1]);
console.log(hh[2]);

// undefined
let jj = undefined;
console.log(jj);

// null
let ii = null;
console.log(ii);

// type checking การตรวจสอบชนิดข้อมูล
console.log(typeof aa);
console.log(typeof dd);
console.log(typeof ee);
console.log(typeof ff);
console.log(typeof gg);
console.log(typeof hh);
console.log(typeof jj);
console.log(typeof ii);