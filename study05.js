// Arithmetic Operators + - * / % ++ -- **
console.log(1 + 1);
console.log(1 - 1);
console.log(1 * 1);
console.log(1 / 1);
console.log(1 % 1);
console.log(1 ** 1);

// Comparison Operators == === != !== > < >= <=
console.log(1 == 1);
console.log(1 === 1);
console.log(1 != 1);
console.log(1 !== 1);
console.log(1 > 1);
console.log(1 < 1);
console.log(1 >= 1);
console.log(1 <= 1);

// Logical Operators && || !
console.log(1 && 1);
console.log(1 || 1);
console.log(!1);    



